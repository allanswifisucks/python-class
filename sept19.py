while True:
    num1=int(input("num1\n"))
    op=input("op\n")
    num2=int(input("num2\n"))
    if op=="+":
        print(num1+num2)
    if op=="-":
        print(num1-num2)
    if op=="*":
        print(num1+num2)
    if op=="/":
        print(num1+num2)
    if op=="break":
        break
